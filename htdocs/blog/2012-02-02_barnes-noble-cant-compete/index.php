		<h2>Why Barnes & Noble Can't Compete With Amazon</h2>
		<div id="published-date">2012-02-02</div>
	</div>

<p>
	The inspiration to write this article came after I read the The New York
	Times article
	<a href="http://www.nytimes.com/2012/01/29/business/barnes-noble-taking-on-amazon-in-the-fight-of-its-life.html">
	The Bookstore's Last Stand</a>, which discuss the current position of Barnes
	& Noble and how they are trying to compete with the giant Amazon.
</p>

<p>
	I really have a love for 7" tablets and as I was planing my wish list to buy
	when I travel to the US I thought about looking at Barnes & Noble web site
	to learn more about their
	<a href="http://www.barnesandnoble.com/p/nook-tablet-barnes-noble/1104687969">
	Nook Tablet</a> so I could decide if I should get one. Of course I looked at
	it just because of the price and the design, for some reason I love virtual
	buttons on a tablet, which is very different than my actual Galaxy Tab 7"
	that is just a bigger Galaxy S.
</p>

<p>
	After I read some reviews I added it to my wish list. I'm going to have no
	problem on buying it since I'm going to travel and I'll buy directly from a
	store, but if I wasn't it would be impossible to get one from outside the
	US. The thing is that B&N site has restrictions and one of those is that you
	can't buy anything if you're not a US citizen with a US address and credit
	card. This means that after I get my hands on the tablet I will not be able
	to buy books, videos, apps, whatever. This doesn't botter me since I was
	going to root and install CyanogenMod 7 on it anyway, but for a normal
	customer this is very bad.
</p>

<p>
	If I was a normal brazilian going to the US to have a great time and decided
	to buy a Nook Tablet there because I was randomly walking by the road and
	saw an ad at the front of the B&N store, I would be screwed as soon as I get
	back to Brazil or the moment I try to download an app. This is why I think
	Barnes & Noble will fail to compete with Amazon, because if it was a Kindle
	Fire I would at least have the ability to download books without any
	problems.
</p>

<p>
	The Amazon Kindle is starting to get some attention here in Brazil and I see
	a lot of people getting them. It's time for Barnes & Noble to rethink their
	strategy and label as urgent their plans to go worldwide.
</p>

