<h2>when did you first get interested in technology?</h2>

<p>
	To be perfectly honest can't remember a time when I haven't been interested
	in technology. Some of my first memories are of me playing with the family
	computer, being fascinated by television, and breaking things apart to look
	inside. Judging by the home movies my parents made, even before I can
	remember, I was already fascinated by machines (tractors, cars, etc.), power
	tools, and circuit boards, or "electrical cities" as I have been recorded
	saying in one of the family's tapes.
</p>

<p>
	I have vivid memories of playing with (most likely completely trashing) the
	family's Windows 95 computer when I was 4, even though I didn't know how to
	read yet, I clicked things, watched what happened, and memorized the icons.
	I was able to open games, shutdown the computer, an play many hours of
	thrilling games such as Microsoft Word, Excel and PowerPoint, Minesweeper,
	and <a href="https://en.wikipedia.org/wiki/Treasure_MathStorm!">Treasure MathStorm!</a>,
	which to this day I still don't know how it ended up in that computer. I had
	no clue what I was doing, but I was happy enough that I could play with such
	amazing technology.
</p>

<p>
	My interest in technology during my early childhood was mostly around using
	computers and tearing down every piece of electronics equipment that was
	gifted to me (didn't matter if it was working or not before I opened it). It
	was joke amongst my family that, if you gave me an RC car, I would simply
	remove the motors and have more fun attaching batteries to them than
	actually driving it around it around, as happened many times.
</p>

<p>
	When I got into my early teenage years I got fascinated with mobile
	computing, something that to this day still fascinates me, and for a very
	long time, probably until my 20s, all I lusted after were Palms. Pocket PCs
	and smartphones such as Nokias, WM-era HTCs, and eventually Android and iOS
	when those came out.
</p>

<p>
	Eventually, when I was about 17 a new interest in technology was found when
	I decided I wanted an Arduino kit for Christmas. After playing around with
	electronics I never stopped, computers became boring, and this interest led
	me to pursue a carrier in electronics design, shaping most of my adult life.
</p>

<p>
	After this reflection, I can safely say that my early interest in technology
	is what has basically shaped my entire life.
</p>

<h2>what's your favourite piece of technology all-time?</h2>

<p>
	This is a tricky one, since technology has always been such an integral part
	of my life, it's very difficult to pinpoint a specific piece of technology
	that I can call a favorite one. There has simply been way to many favorites
	along my life.
</p>

<p>
	Although, after some careful consideration I would say that definitely the
	piece of technology that has been with me for the longest time, and that has
	shaped many of my decisions, is always in the back of my head, and that I
	try to insert into as many contexts of my life as possible, has been and
	will always be the
	<a href="https://www.hpcfactor.com/reviews/hardware/hp/jornada720/">HP Jornada 720</a>.
</p>

<p>
	This little Handheld PC has been a love of mine ever since I got one used
	on MercadoLivre (eBay of Brazil) back in 2006. It's what I still believe a
	pocket computer should be in terms of size and ergonomics. It's an amazing
	piece of technology and was responsible for making me learn C Win32 API
	programming and Visual Basic, simply because I loved it so much that I
	wanted to extend its capabilities with my own software.
</p>

<p>
	Right now I own 3 of them, only one in working condition, and I guess I
	should buy more of them to ensure I always have a working one.
</p>

<h2>what's your favourite piece of technology right now?</h2>

<p>
	Right now I can safely say that my favorite pieve of technology is my
	<a href="https://en.wikipedia.org/wiki/Sony_Vaio_P_series">Sony VAIO VGN-P11Z</a>.
	It has always been a dream machine for me ever since I saw one at a
	<a href="https://en.wikipedia.org/wiki/Fnac">Fnac</a> store in Barcelona
	while on a family trip in 2009. Looking back it's obvious that I would love
	this machine, since it's basically a modern version of the Jornada 720.
</p>

<p>
	Ever since I finally got mine in January of 2025, after all these years and
	them never coming down in price, I have been inserting it more and more into
	my workflow, and frequently using it at work for on-site CAD and note
	taking tasks.
</p>

<h2>name one new cool piece of technology we'll have in 25 years!</h2>

<p>
	It's funny that until around 2017 I would be thrilled to answer this
	question and would probably have many ideas and wishes, but today I do
	believe technology has reached its peak, every day I look more forward to
	older technologies and integrating them into my modern way of living, than
	looking forward to what may come in the future.
</p>

<p>
	With this in mind, I would say the coolest piece of technology that I wish
	we can all have in the future is proper AR glasses, kind of like
	<a href="https://en.wikipedia.org/wiki/Google_Glass">Google Glass</a>, but
	with an interface similar to the
	<a href="https://en.wikipedia.org/wiki/Apple_Vision_Pro">Apple Vision Pro</a>.
	Although I know that whenever that dream materializes it will simply be some
	depressing tracking device, fully locked, and without any soul apart from
	some gimmicky AI garbage, just like every piece of technology we have today.
</p>

<h2>closing words</h2>

<p>
	I would like to end this on a hopeful note. Even though the future looks
	bleak, technology seems to have reached its peak, and
	<a href="https://en.wikipedia.org/wiki/Skynet_(Terminator)">Skynet</a> is
	right around the corner, always keep being a curious person, it's what has
	motivated me throughout my entire life. Curiosity is what has made humans
	get to where we are today. Curiosity is what has made technology possible,
	and is what should keep moving you forward, even if it makes you look at
	technology backwards.
