<p><em>I'm watching the <a href="http://live.theverge.com/microsoft-live-blog-tablet-announcement/">Microsoft keynote</a> at the same time as I write this, so if I commit any error just put it in the comments.</em></p>

<p>List of problems with the Microsoft Surface:</p>

<ul>
	<li>16:9 aspect ratio</li>
	<li>Confusing products (ARM and Intel)</li>
	<li>No pricing confirmed</li>
</ul>

<p>The 16:9 aspect ratio for a 10&quot; tablet is the worst one you could ever use and still Android manufactures and Microsoft thinks it's a great thing. It's just impossible to feel comfortable with a 10&quot; 16:9 tablet in your hands while in portrait and the same happens for landscape. Every time I get my ASUS Eee Pad Slider to do something I immediately want to go back to my iPad.</p>

<p>Having the same name for completely different products that look exactly the same is the worst thing Microsoft could ever do. They made a professional tablet, using a Intel chip, running Windows 8 Pro (another terrible name) that "might be capable" of running all those those so called "classic" applications. And another tablet that looks just the same but with a ARM chip and running Windows RT, and won't be capable of running "classic" applications, which I've <a href="http://nathancampos.me/post/17091842348/windows-8-is-already-fragmented">already discussed how it will be a nightmare on a past article</a>.</p>

<p>The last thing that wasn't presented and also haven't talked about is the availability. I'm almost sure it won't be available on developing nations like Brazil neither countries that don't have Microsoft Stores.</p>

<p>Anyway I won't be buying one of these because I seriously don't care about Windows anymore, it's boring since Windows Vista and it's still boring, but now it will be a lot worst with all the fragmentation problems that are going to come with the release of Windows 8 and Windows RT.</p>

<p>Some interesting quotes I selected:</p>

<blockquote>We believe that any intersection between human and machine can be made better when hardware and software are considered together.</blockquote>

<p>Looks a lot like the Apple approach huh?</p>

<blockquote>Take the mouse. Windows needed one, so we built one. Early reviews were not very positive &mdash; in fact, it was so new that Canadian customs quarantined it.</blockquote>

<p>I won't comment on this one.</p>

<blockquote>"I say perfect a lot &mdash; it's part of our team culture."</blockquote>

<p>I still remember Windows Vista.. Also that's not part of your team, it's more part of the Apple team.</p>

<blockquote>We took the time to get Surface and Windows 8 right. To do something that was really different and really special. We're proud of the Surface like we're proud of Windows 8. Because of Windows 8, the Surface is a PC, it is a tablet&hellip; it's something new.</blockquote>

<p>Looks like a Transformer Prime running Windows 8 for me&hellip; </p>

