<p>Two days ago I came across this: <a href="http://www.google.com/hostednews/afp/article/ALeqM5ipkAQJJNgj69HxLbKppH80WjUQJg">Orange &lsquo;forces Google' to pay for mobile traffic</a>. Clearly Orange has no idea how the internet works and what's their involvement in it. The ISP is just a dumb pipe to connect the user to a server, nothing more. If their users are using too much bandwidth and the costs are higher than the profits they should start to rethink the price of their service, not go after Google saying that they are evil for transferring all that data.</p>

<p>We should also mention that if Orange, and other ISPs, start to rethink their service costs they should never throttle the connection. The user fees should be equivalent to their connection speed, not their usage, which apparently is something that <a href="http://en.wikipedia.org/wiki/Bandwidth_throttling#ISP_bandwidth_throttling">ISPs can't understand too</a>.</p>

<p>In my opinion the bigger problem here is that Google apparently accepted to pay the fees (even worst since the government is also forcing a decision favorable to the ISPs):<blockquote></p>

<p>French President Francois Hollande warned Google on Wednesday that his government would legislate a so-called Google tax if the company doesn't reach a deal with French media companies.</blockquote></p>

<p>If they accept to pay these fees they are going to open the gates to other ISPs to demand the same compensations. This is a completely anti-net-neutrality move, which goes in the inverse direction of Google's ideals.<blockquote></p>

<p>Google has also been faced with demands for compensation from content providers such as newspapers, who charge the search giant makes lots of advertising revenue from referencing their material.</blockquote></p>

<p>What?! If you put such material on your website and <strong>allow</strong> Google to index it so people can find you, which will generate profits over the ads you have your website, you should instead pay Google (if you don't use AdSense) for making it a lot easier to generate traffic to your ads.<blockquote></p>

<p>Hollande said "Those who make a profit from the information" produced by media companies should participate in their financing.</blockquote></p>

<p>If the information isn't already publicly available, on the media company's website and they allowed Google to index their information, there's no way Google should pay for the profits they are making with ads.</p>

<p>If we really want the internet to continue as it was intended to be companies that are facing these issues should promptly oppose them and make their opinion public, just like what happened with SOPA and PIPA last year. Otherwise things are only going to get worst than they already are. </p>

