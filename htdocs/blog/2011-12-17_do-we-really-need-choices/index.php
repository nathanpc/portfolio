<p><em>Disclaimer: If you're a Android fan boy close this window right now.</em></p>

<p>I've been a truly Android fan boy for 2 years and a Apple hater for 5 years, but this year I did the impossible: Bought an iPad, a Macbook Pro and an iPhone. This acquisitions really changed the way that I saw Android and all the other platforms that I loved. On this article I'll be talking a bit about the most used argument by Android users to say that their platform is better: Choice. They say that you there are a variety of devices on the market that runs their OS so you can choose the one you like most, also that you can customize Android, flash new ROMs and tweak everything you want of it. While Apple&hellip; Yeah, you know how closed they are.</p>

<p>I want to start this sentence by making you deeply think "Do we really need choices?", I'm sure that if you think really deep on this you will end up in the same point I had. We don't really need to have choices, all we need is a smartphone that has apps, games, email, phone, etc. (the same principles as the first generation smartphones like my old Nokia E61).</p>

<p>After thinking about this I realized that this got dramatically stronger with tablets. People are buying tablets and don't even know what they will do with them. When someone asks me which is the best tablet from them I ask "What are you going to use it for?" and everyone is like "Really&hellip; I don't know". This happens on phones too, people just buy a new phone for no reason. And all this non-sense arrives at choices. No one really want a "different phone", people just buy the phone that looks cool, don't matter if it has a 5&Prime; or a 3.5&Prime; screen, if it runs Android, Symbian, Blackberry OS or iOS, they will buy it anyway. Do you really think that "normal persons" (which means people that aren't tech savvy) will customize their Android phones? Of course not, they won't install a new launcher, they will only change the wallpaper and add some widgets to the home screen and that's all!</p>

<p>Apple is doing it right by restricting the users to the same screen size/ratio and keeping the design of their hardware and software almost the same since the first generations of their devices (this includes iPods, iPhones and Macs) is the best way to go. A great example of this on Android is the Nexus lineup, the "pure Google experience". Those devices get the updates first and have the interface that Google originally made (which means no skins on top of it, just pure vanilla), just because they "locked" the user options into one line of (geeky) products that they can easily manage, which leads to the biggest problem that Android has today: Fragmentation.</p>

<p>All these "choices" are leading to awful experience on the software side and on the developer side. An example of the user experience side is that Dead Space works on the Verizon Galaxy Nexus, but it won't on the GSM version, don't forget that we are talking about a Nexus phone, which should be the less-fragmented experience possible. On the developer side it's very very awful because you must design your app to work on all the different screen sizes/ratios, hardware and also skins that companies love to put on their devices.</p>

<p>If you're a gadget freak, like me, that buys a lot of devices, you might have already felt the same as I've described on this article. We don't need more choices, we need more standards.</p>

