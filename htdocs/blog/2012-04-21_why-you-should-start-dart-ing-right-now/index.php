<p>With this article I hope you understand more about the <a href="http://dartlang.org/">Dart</a> idea and how you can become more productive and happier as a developer of web applications by using it as your main language.</p>

<p>First of all here are some words, from the official site, for you to understand what Dart is:<blockquote></p>

<p>With the Dart platform, you can write code that runs on servers and in modern web browsers. Dart compiles to JavaScript, so your Dart web apps will work in multiple browsers (not just ours).</p>

<p>The Dart platform includes a language, libraries, an editor, a virtual machine (VM) for both servers and browsers, and a compiler to JavaScript.</blockquote></p>

<p><strong>The Community is Awesome</strong><br />One of the biggest advantages of Dart is the vibrating community. Since it's still under development the developer base is very helpful and shareful too. If you're on Google+ you'll know almost every new project that is done using Dart, this way you have more attention on your project.</p>

<p><strong>Libraries are Needed</strong><br />Because it's on it's early stages Dart needs libraries to fulfill developers, this way you have a lot of libraries you can create, from scratch or porting from Javascript. This way you'll be learning more, helping the community and earning respect from the community.</p>

<p><strong>It's Incredibly Easy to Create Web Applications</strong><br />Since it was built by Google for web applications from scratch, it's the perfect language for web application development. You're not having to adapt a old language for the web revolution, you're using a language that was built for the web revolution.</p>

<p><strong>Weekly Updates</strong><br />The language is under active development. Almost every single week a new build of the language, or the tools (Dart Editor and Dartium), is released. This way you can feel something that any other language can provide you today, the feel that it's mutable and is evolving. It's awesome when I open my Google Reader application and there is a new update on the Dart feed. I quickly download and test what's new.</p>

<p><strong>Organized Syntax</strong><br />As a node.js developer I know that as you dive deeply into a complex part of your web application the code becomes a hell to understand. With the Dart syntax this is a bit more difficult, your code becomes more readable and a lot more maintainable. Also the Dart team published an awesome <a href="http://www.dartlang.org/articles/style-guide/">style guide</a> to make your code even better for your eyes and brain.</p>

<p><strong>Easy to Learn</strong><br />Dart is incredibly easy to learn. There are three ways of learning Dart. If you're already a Javascript you can learn by reading the awesome <a href="http://synonym.dartlang.org/">Translations from Javascript</a> table which will show everything you must know about Dart comparing to Javascript. The second way of learning is by reading their lengthy <a href="http://www.dartlang.org/language-tour/">Language Tour</a> which will give you an in-depth look at every single aspect of the language. The third way is to learn by practicing and looking at the previous links when you don't know how to do something or the compiler is giving you a strange error.</p>

<p>Those, in my opinion, are some of the key features of the awesome Dart language. I hope you give it a try and have lots of fun with it. Don't forget to comment about your experience with Dart and your opinion about it. </p>

