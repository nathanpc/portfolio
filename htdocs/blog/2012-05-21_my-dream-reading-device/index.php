<p>Today I was reading some of the 109 articles saved on my <a href="http://getpocket.com/">Pocket</a> account and I thought about something that I would love: A tablet running a fully customized (for stability and lightness) version of Android sporting a awesome e-ink display. As soon as possible I <a href="https://plus.google.com/u/0/112969911133615369021/posts/RVz3GRHvgw2">posted it on Google+</a>, since I really wanted to philosophize more on this idea I've wrote this article.</p>

<p><strong>Why e-ink?</strong></p>

<p>First of all, if you're going to read for long periods of time the LCD screen is just going to burn your eyes, that's why e-ink is the best alternative. Second, if you ever owned a Kindle (I own the DX, and the new non touch screen version), or any other e-ink device, you know that the experience is incredibly great. I know the refresh rate is a con, but seriously there are a lot more pros and also this technology is still evolving.</p>

<p><strong>What about a touch screen?</strong></p>

<p>Maybe, but I would buy the non-touch screen version because on a reading device I prefer to navigate and switch pages using physical buttons, also be able to holding it anyway I want without worrying about touching the screen by accident.</p>

<p><strong>Why not just root a Nook Simple Touch?</strong></p>

<p>Yeah, the Nook Simple Touch <a href="http://forum.xda-developers.com/showthread.php?t=1346748">can be rooted</a> and turn into a "fully functional" e-ink Android tablet, the problem is fairly simple: The lack of Android e-ink optimized apps. That's why if a startup start this trend and it gets some attraction of reading addicts I'm sure developers of big reading apps for Android will optimize their applications for this new category. Also the interface isn't actually optimized, it's just a lighter version of ADW Launcher with some tweaks, nothing drastically changed from stock Android, and for this kind of screen a UI with more contrast and less mid-tones is a must.</p>

<p><strong>What's your proposed interface for this device?</strong></p>

<p>This is my sketch of the perfect UI for it, <a href="http://www.fiftythree.com/paper">made with Paper</a> (sorry for my terrible drawing skills):</p>

<?= compat_image("./android-eink-sketch.JPG", "UI Sketch") ?>

<p>The interface is pretty clean and extremely usable (also it looks a bit with the Kindle interface). The status bar will only show the name of the user and a clock. Below the <em>status bar</em> is the <em>app drawer</em>, I thought about it as just a simple collection of the application names (scrollable if you have more applications than it can fit on the screen). The last piece is the actual running application itself.</p>

<p><strong>There is market for such product?</strong></p>

<p>That's a difficult question to answer, but as far as everyone can see the eReader/eBook market is growing exponentially and I think a lot of customers would give it a try if it was presented to them.</p>

<p><strong>What you might end up doing?</strong></p>

<p>As I wait until this dream reading device becomes true I'll buy a Nook Simple Touch, root it, customize the OS to make everything with high contrast, and start developing that launcher from scratch.</p>

