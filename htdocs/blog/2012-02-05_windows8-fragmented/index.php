<p>
	As many of you already played with Windows 8 Developer Preview, and if you
	didn't at least you know it well from tech sites, it's already a fragmented
	OS and in my opinion it's more fragmented than Android. Maybe a lot of
	things will change when the final version comes out, but I don't think this
	will happen.
</p>

<p>
	I'm talking about the two OSes that come on the Windows 8 pack. One has a
	Metro UI with all the new and cool stuff borrowed from Windows Phone and
	built for a tablet, since it's very bad to use that interface with a mouse.
	The second is the good, but old, Aero interface which we all know is perfect
	for a mouse. If you want to have a complete new interface you should use it
	across all the apps. On Windows 7 I could run any app built specifically for
	XP and it would have the same Aero look as any other Windows 7 app.
</p>

<p>
	Now imagine at the user side, someone that is just ok about computers and
	think that OS = Windows. This person will need to relearn almost everything
	about Windows all over again and deal with apps that must run on Metro and
	others that only run on Aero. Having to switch between them over and over
	will make the user feel uncomfortable about the OS because it doesn't have
	the consistency to stay in a single type of interface.
</p>

<p>
	And what about Windows on ARM? Of course apps compiled for x86 won't run on
	ARM-based Windows machines, and vice-versa. This will add another layer of
	fragmentation on Windows, maybe more work for the developer too if it the
	ARM version of the SDK has limitations.
</p>

<p>Microsoft is fragmenting Windows before it even gets released.</p>

