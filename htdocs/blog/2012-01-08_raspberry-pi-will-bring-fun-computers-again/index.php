<p>I was browsing the Raspberry Pi forums these days and I came across a very interesting thread titled <a href="http://www.raspberrypi.org/forum/off-topic/pcs-are-boring/">PC's Are Boring</a>. I read all the posts until that moment and started reflecting about that statement. The thread starter was completely right about this, PC's (which I understand for computers that run Windows or Linux, excluding Macs) are really boring, that's why the mobile industry is so amazing these days, because people stopped changing their computers every &frac12; years and started changing their mobiles.</p>

<p>A lot of the users on the forum were talking about "the old times" of the Commodore and Atari when you felt like you had power over the machine and today you're just part of a mainstream movement. Also they were talking about how "normal people" are discouraged to program because are afraid they can break the computer (which isn't true of course, but that's what the average user thinks) and how the price of the Raspberry Pi could help people to get into Linux or programming. They are completely right, as soon as the Pi comes out a lot of programmers are going to rush to get their hands on one (I am very excited to get my hands on one too) and possibly a lot of people that want to start programming will get it too.</p>

<p>The RPi will make the feeling of having power over the machine come back again. The best example I can give is my own. I've never been so excited for a "computer" since the first dual cores came out, I'm thinking about the awesome things that I could do with it like: Making my own Linux-powered tablet (which is completely possible), porting new Linux distros to it, porting other OSes to it and even making my own distro only for the Raspberry Pi.</p>

<p>I'm sure all the geeks are very excited waiting for the release and wondering all they could do as soon as they get their hands on it. Leave a comment below with your opinion or ideas. If you want to keep in touch to the latest news about the board just visit their <a href="http://www.raspberrypi.org/">blog</a> and don't forget to contribute on the <a href="http://www.raspberrypi.org/forum/">forums</a>.</p>

