		<h2>The Amazon Kindle Fire</h2>
		<div id="published-date">2011-10-23</div>
	</div>

<p>
	After the mighty success of its Kindle e-readers, Amazon has previously
	announced its new tablet: the Amazon Kindle Fire. Based off of a heavily
	modified version of Android, this new tablet will not only have e-books but
	also movies, magazines, TV shows, songs, and apps. It will be released on
	November 15 of this year for the small price of $199 compared to the iPad's
	$500+ dollar price tag. What will come of this new tablet? Does it have the
	ability to overtake the iPad as the prominent tablet? Allow me to weigh in
	my thoughts and review this exciting development.
</p>

<p>
	Based on pre-release purchases, the Kindle Fire could outsell the iPad in
	first month sales. This may be influenced by the staggeringly lower cost.
	The Kindle Fire will also have access to Amazon's leading cloud storage for
	no extra cost, allowing you to store your content in the cloud. You have the
	cloud storage in addition to the 8GB hard drive built into the Fire which
	only really serves to store non-Amazon content with everything else being
	hosted freely.
</p>

<p>
	Secondly, there comes the matter of apps and other programs. The Apple app
	store is too closed and the Android store is too open. The Amazon app store
	hopes to solve these problems by remaining an open platform like the Android
	store but only making the best apps available. Since the Fire is running a
	modified version of Android, there won't be a giant learning curve to
	writing apps for it. As for specific apps, you will be able to get Pandora,
	Twitter, Facebook, and Netflix. Streaming content will most likely end up
	being a huge part of the Fire's usage.
</p>

<p>
	The Kindle Fire that will be released in November will only have a 7-inch
	screen, about two inches smaller than the iPad's, but in early 2012 Amazon
	will reportedly release a version with a 10-inch screen. Jeff Bezos,
	Amazon's CEO, stated that people should not see the Fire as a standalone
	device, rather part of an integrated media service. "That's why the Kindle
	has been successful," he says.
</p>

<p>
	The Fire is introducing a new style of web browser: a split browser. Dubbed
	"Silk" because that substance is almost invisible yet really strong, the
	Fire takes advantage of Amazon's powerful data centers to process and fetch
	parts of pages before sending them to the client. By utilizing caching and
	this split browsing, the Silk browser on the Kindle Fire will be able to run
	much faster and efficiently.
</p>

<p>
	A purchase of the device will give you free access to Amazon Prime for a
	year. This service provides faster, cheaper shipping on many items, but it
	also provides free video streaming, a major feature of the Fire.
</p>

<p>
	In conclusion, the Amazon Fire seems to be a strong contender in the tablet
	market with many features which set it apart from the crowd. Only time can
	tell which will remain victorious.
</p>

