<p>
	I've received my new Kindle, the $104 international model (there is a $79
	ad-supported version for US customers), and I think that it would be nice to
	make a simple first impressions/fast review on this great device. I won't
	make a full review since a lot of the big tech sites already made them about
	3 weeks ago. This is just for people that want to read another opinion on
	this device.
</p>

<h3>The Device</h3>

<p>
	Since the moment I first held it in my hands I instantly loved the feel of
	this new and smaller version. It's as thin as the old models, but it's
	lighter and better for holding. The display quality is great, the e-ink
	refresh rate is still the same, but it's a lot faster.
</p>

<h4>Hardware</h4>

<p>
	All the buttons on the bottom of the device are well placed and you won't
	have any problems to use them, but following the Kindle Fire style, the
	power button is located at the bottom of the device, which makes it to
	accidentally switch off if you hold it vertically on a table, usually this
	might happen when you're showing it to someone since there is no reason you
	would be reading it vertically on a table.
</p>

<p>
	The page forward and backward buttons aren't in the best position. If Amazon
	placed only a big forward button on the right and the backward/forward (the
	inverse as it is right now) at the left it would be perfect, but as they are
	you won't have any problems to do it, just a little (very little, almost
	micro) discomfort to hold it and press the buttons at the same time.
</p>

<h4>Software</h4>

<p>
	This version comes with 4.0, but I recommend you to update to 4.0.1 if you
	are an update-freak (like me) or if you want the option to refresh the e-ink
	every page turn (which I don't want). By the way there is nothing much to
	say about the Kindle software since it's pretty simple and the same on all
	the other Kindle devices.
</p>

<h4>Comparison With The Kindle DX</h4>

<p>
	Comparing with my Kindle DX it's of course a lot smaller. Both devices have
	the same thickness, but the new version feels thinner when you hold. I
	really won't miss the keyboard, since I almost never used it in my DX. Also
	the display quality is a lot better than the DX one.
</p>

<h3>The Case</h3>

<p>
	I'm sure there is nothing much to say about a case, but it's something that
	I haven't seen any other sites commenting on. Anyway, I got my new Kindle
	bundled with the <a href="http://www.amazon.com/dp/B005HSG446/">Marware
	Eco-Vue Cover</a> which is a great cover for your Kindle (it can be used in
	the Touch version too).
</p>

<p>
	It's great for protection and it's very well made, with great leather finish
	outside and a very comfortable soft leather inside. It also has a little
	pocket inside if you want to carry some paper notes with you and the Kindle
	won't fall from inside, since the holding system was very well designed.
</p>

<p>
	Holding your Kindle while it's inside this cover isn't very good, maybe it's
	because the case is brand new and the leather is still a bit hard to stay in
	the open position. This case also adds some weight and thickness to your
	Kindle, that's why I'll only use this case to carry my device, while reading
	I'll take it off the case.
</p>

<h3>Conclusion</h3>

<p>
	If you currently have the first generation Kindle or the second generation
	one you should definitely get this since it's a huge milestone, but if you
	have the third generation or the DX I would suggest you to upgrade only if
	you want a device that is smaller and better to carry with you.
</p>

<p>
	I'll keep my Kindle DX and use it for technical reading and PDFs. I bought
	this only because I was needing a eReader to carry with me all time, which
	isn't possible with the Kindle DX, since it's a very big piece of hardware.
</p>

