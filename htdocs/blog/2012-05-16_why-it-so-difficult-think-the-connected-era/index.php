<p>This week I woke up with a awesome idea: Almost an entire first chapter of a sci-fi book, so I decided that I should wrote this book. Also I always had this desire of writing an entire book that would include all my theories about the society.</p>

<p>A week passed and all my progress at the time is 2 pages (A4). What happened? It's difficult to have more ideas. On the era that we are connected 24/7 and work, studies and all our "offline" activities consume our time, minds and ideas, it's difficult to think since our minds and thoughts are occupied with other "more important" activities. That's why most of my great ideas and thoughts come to my mind while I'm not connected or doing any other activity (not that I haven't mentioned "worried" about the activities), they usually come when I'm going to sleep, taking a bath or on the car. A great example is this article itself, I thought and wrote it on the car.</p>

<p>So here is a tip to end this story: If you want to have more/better ideas all you have to do is go "offline" and try to distract yourself from any other activity that might keep you away of thinking. Listening to a so g you love is also a good idea to have ideas.</p>

<p>Do you have the same problem of having ideas because of all the activities and distractions of the connected era? I would love to hear your opinion. </p>

