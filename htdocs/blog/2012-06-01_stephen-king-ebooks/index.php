<blockquote></p>

<p>"I love crime, I love mysteries, and I love ghosts, I also loved the paperbacks I grew up with as a kid, and for that reason, we're going to hold off on e-publishing this one for the time being. Joyland will be coming out in paperback, and folks who want to read it will have to buy the actual book."</blockquote></p>

<p>After Stephen King made that announced all the news sites when crazy to report it. I got the news pretty fast since my Twitter feed got flooded with the news coming from the most varied sources. When I started reading the articles about it I got shocked, how someone can do such thing in the middle of the eBook revolution? A lot of people, like me, <a href="http://www.reddit.com/r/ebooks/comments/u59yb/cant_read_paper_books_anymore/">prefer to read on eReaders</a> than on the actual book, also there are a lot of people that prefer because it's easier and more convenient to buy and digest the content of the eBooks.</p>

<p>I know that Mr. King (and Mrs. Rowling in the past too) is extremely famous and has the power to decide such thing, but letting a entire segment of the market, which was already used to your eBooks, without your new piece is just unfair. Many authors make this move because of disagreements with online stores, but those usually don't affect well-known authors, usually just the small ones that really need to generate some revenue from their eBook sales.</p>

<p>eBooks seriously revolutionized the 600 years old industry. For the first time in the history we can buy books on affordable prices and with extreme comfort, this made the reading habits of many grow, which contributes to this generation to have more access to culture. Denying or delaying too much the release of an eBook is an act of denying knowledge and culture. The eBook revolution is future and in the future there is no place for paper.</p>

