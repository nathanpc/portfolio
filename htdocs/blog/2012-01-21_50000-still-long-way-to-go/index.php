<p>
	Just 4 weeks ago the Windows Phone Marketplace
	<a href="http://allaboutwindowsphone.com/news/item/13913_Windows_Phone_Marketplace_pass.php">
	reached the 50k applications milestone</a>, but there's still a very long
	way that Microsoft should go until they reach a good place in the "App Store
	wars". A lot of must-have apps on iOS and Android aren't available for
	Windows Phone, simple things like a oficial
	<a href="http://readitlaterlist.com/">Read it Later</a> client or a decent
	free Twitter app (The oficial one doesn't include notifications). I've been
	using my HTC Ultimate (aka Titan) for 4 weeks and I'm loving the OS, but
	missing a lot of apps from my Android phones and iPhone, that's why I
	constantly take them with me along-side with my Windows Phone.
</p>

<p>
	For sure Microsoft should be celebrating this, but now that they got a bit
	more attention from developers they should start to promote their platform
	more to startups like GetGlue and Read It Later, also get big companies like
	Google, Dropbox and Adobe to their suite of apps to Windows Phone. Many tech
	journalists say that they love Windows Phone, the only down-side are the
	apps which are mostly unavailable for WP.
</p>

<p>
	As a developer, I'm trying Microsoft Visual Studio for Windows Phone and I'm
	loving the GUI designer and how connected the tools are, different from
	Android which is a mess for development (in my opinion). The only problem I
	found on C# is that it's a bit difficult to make simple things like get a
	XML response from a API asynchronously and populate a List using the
	information. (Still haven't figured out how to make it)
</p>

<p>
	A good way to attract developers to a platform is to make a lot of tutorials
	and screencasts to help beginners moving from other platforms like Android
	or iOS. There are just a few books out there from O'Reilly and Wrox Press
	about Windows Phone development, but still not a single screencast. The
	tutorials at MSDN Blogs are good, but still only a few of them and I'm still
	looking for a good tutorial about XML handling to use more of my development
	time for Windows Phone.
</p>

