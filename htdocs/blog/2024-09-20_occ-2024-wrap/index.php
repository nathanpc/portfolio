<p>This post has been very long in the making, as you can clearly notice by the
timestamp in it, exactly 2 months have passed since my last OCC update and a lot
has happened since then. Even though I missed my deadlines for the OCC posts it
wasn't because I failed, quite the opposite, it was because I was having so much
fun that I was simply not bothering to write a blog post. Although after all
this time I did feel that a wrap-up post was needed.</p>

<p>During the last couple of days of my OCC week I talked a lot to my fellow
peers over IRC and worked almost non-stop on my Gopher client
<a href="https://bb.deadnet.se/viewtopic.php?t=94">Rodent</a> and its Win32
port, so there wasn't a lot to share as well apart form some Visual Studio
screenshots and chat logs. I do believe this was also one of the reasons why I
wasn't posting any updates.</p>

<p>After the end of my OCC week I continued working on the Gopher client and
got it to a point where I was confortable
<a href="https://bb.deadnet.se/viewtopic.php?p=532#p532">releasing binaries for
the Win32 port</a>. I also quickly ported the project to Visual C++ 6, so now
the WIn32 port is capable of running on Windows 95 through Windows 11.</p>

<p>One of the interesting aspects of the OCC was that afterwards I felt like I
was almost in a perpetual OCC, I even talked to some other members in our IRC
channel and some are feeling the same way. After this experience we all noticed
how usable some of these old machines are and realized that we should fire up
our retro computers more often, not just play around with them occasionally, but
if you have some free time, actually enjoy it using an older machine.</p>

<p>After that amazing week I've been using my retro computers a lot more and I'm
constantly thinking of ways of forcing myself to use them more as well. One
clear example of this is the fact that I'm writing this blog post from my Power
Mac G5, the same one I did a lot of OCC from, simply because I felt like using
it, something I woudn't have been so inclined to do previously. I do believe
every retro collector should try to have an "off season" OCC to properly
experience their computers but also to get this "retro bug" and use their
machines a lot more frequently. If any of you actually do this be sure to join
the <code>#oldcomputerchallenge</code> channel on
<a href="https://libera.chat/">Libera.Chat</a> and let me (nathanpc there) know
about it, I'm sure you'll enjoy your stay there.</p>

<p>Apart from using my retro machines a lot more these days, something extremely
positive that I got out of OCC was a new set of online friends and an amazing
community to hang around with. I think this was the best part of OCC. After it
all ended most of the participants stayed in IRC and we chat regularly. This is
very refreshing for me and I feel great about having fun talking endlessly to
like-minded people on IRC, something I no longer felt for at least 10 years.</p>

<p>I would like to end this post by thanking everyone that participated in OCC
this year, the organizers that put on such an amazing challenge and a special
thanks to the amazing
<a href="https://bb.deadnet.se/memberlist.php?mode=viewprofile&u=2">Headcrash
</a> for inviting me over to the challenge, I wouldn't have had this amazing
experience if it weren't for you.</p>

