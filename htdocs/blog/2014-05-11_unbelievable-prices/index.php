<p>Since I'm building a bunch of audio circuits lately and I usually have to test them using a "normal" sound source (after extensively testing it with my function generator), I decided I should buy the cheapest chinese crap MP3 player just to protect my phone or any audio source that I'm using. The last thing I want is to short something or accidentally apply a (relatively) high voltage to one of the audio pins, or do anything that could damage my device.</p>

<p>So, I quickly went to <a href="http://mercadolivre.com.br/">MercadoLivre</a>, the brazilian version of eBay, and searched for the cheapest ($3 multimeter quality) MP3 player available, and sure enough, <a href="http://produto.mercadolivre.com.br/MLB-554445523-mini-mp3-playerfm-shuflle-clipentrada-microsd-suporta-32gb-_JM">this popped up</a>. It always amazed me how cheap they can produce this sort of crap, but seriously, $6.31 with taxes and all for a device with a shitty color display and all? That's just insane, they are probably assembling millions of those to be able to produce them at that price.</p>

<p>So, in a nutshell, I've bought this piece of junk which will happily suit my needs, since it's so fucking cheap that it can be considered disposable. </p>

