<p>Today I revived my hope on Microsoft as I received the news that they released their brand
new “product” for their fellow developers. The release I am talking about is the official
<a href="http://wmpoweruser.com/developers-tired-of-ios-style-web-apps-microsoft-releases-metro-theme-for-jquery-mobile/">Metro theme for jQuery Mobile</a>. The awesome mobile framework finally got some Microsoft
love.</p>

In December of last year I got a new device to develop for, a HTC Titan, running the
latest Windows Phone 7.5 Mango build. I loved the OS UI and how the applications were
information-centric and not just eye-candy, but there was one problem, the same way C# is
a incredible language for a lot of things, it’s parsing functions, for JSON specifically,
are very difficult to learn and the articles about it were made for senior C# developers,
which makes it difficult for beginners like me to understand them, at least I now can
develop using the technology I love most for mobile development: <a href="http://phonegap.com/">Cordova (aka PhoneGap)</a>.

I always wanted to use Cordova for my WP7 projects but the Metro interface was way too
complex to build from scratch and since the WP7 build of Cordova was on it’s early stages
there were some features still to be implemented, for example there was no way to prevent
the app from scrolling and some other things. Now there are plenty of plugins to make the
app as native as possible and with the latest help from Microsoft, the Cordova development
community has another great platform with almost full support of the native web app
framework.

It’s great to see that Microsoft finally realized that the future of development has
Javascript, HTML and CSS as the main languages. That’s why I have hopes that <a href="https://wiki.mozilla.org/B2G">Boot to Gecko</a>
gets some market attraction and becomes a popular platform.
